public class halfpyramidwithNumbers {
    public static void main(String[] argu){
        int n=5;
        //outer loop
        for(int i =1;i<=n;i++){
            //innner loop
            for(int j=1;j<=i;j++){
                System.out.print(j+" ");
            }
            System.out.println();
        }
    }
    
}
